package com.ecodation.math;

public class MathTuto {

	public static void main(String[] args) {
		System.out.println(Math.PI);
		System.out.println(Math.E);
		System.out.println(Math.pow(2, 5));
		System.out.println(Math.sqrt(25));
		System.out.println(Math.abs(-4));
		System.out.println(Math.min(65, 55));
		System.out.println(Math.max(85, -2551));
		System.out.println(Math.cos(90));
		System.out.println(Math.sin(90));
		System.out.println(Math.acos(90));
		System.out.println(Math.asin(90));
		System.out.println(Math.floor(8.9));
		System.out.println(Math.ceil(8.1));
		System.out.println(Math.round(5.5));
		System.out.println(Math.round(5.4));
		System.out.println(Math.random());
		System.out.println(Math.random() * 5 + 1);
		System.out.println(Math.round(Math.random() * 5 + 1));

		System.out.println(Math.asin(0.6));
		double result = Math.sin(90);
		System.out.println(result);

	}

}
